static void FLASH_EraseSector(void)
{
	HAL_FLASH_Unlock();
	FLASH_Erase_Sector(SECTOR_NO, FLASH_VOLTAGE_RANGE_3);
	HAL_FLASH_Lock();
}


void write_to_flash(uint32_t offSet, void *wrBuf, uint32_t Nsize)
{
	uint32_t flashAddress = SECTOR_ADDRESS + offSet;
	
	//Erase sector before write
	FLASH_EraseSector();				// This will increasing the latency by substancial amount
	
	//Unlock Flash
	HAL_FLASH_Unlock();
	//HAL_FLASH_Program(FLASH_TYPEPROGRAM_WORD, flashAddress + 8, 0xFFFFFFFF);
	//Write to Flash
	for(uint32_t i=0; i<Nsize; i++)
	{
		flag = ((uint32_t *)wrBuf)[i];
		HAL_FLASH_Program(FLASH_TYPEPROGRAM_WORD, flashAddress , ((uint32_t *)wrBuf)[i]);
		//HAL_Delay(2000);
		flashAddress+=4;
	} 
	
	//Lock the Flash space
	HAL_FLASH_Lock();
}


